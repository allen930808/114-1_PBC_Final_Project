import csv
import os

def write_unit_csv(filename, words):
    filepath = os.path.join("data", filename)
    with open(filepath, "w", encoding="utf-8", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["japanese", "reading", "chinese"])
        for jp, rd, cn in words:
            writer.writerow([jp, rd, cn])
    print(f"  ✓ {filepath} — {len(words)} 筆")

